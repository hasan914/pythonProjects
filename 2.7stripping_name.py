name = "\t\n Hasan Javed \n\t"

# Print the original string with whitespace
print("Original:")
print(name)

# Remove whitespace from the left side
print("\nUsing lstrip():")
print(name.lstrip())

# Remove whitespace from the right side
print("\nUsing rstrip():")
print(name.rstrip())

# Remove whitespace from both sides
print("\nUsing strip():")
print(name.strip())